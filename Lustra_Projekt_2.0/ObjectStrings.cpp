std::string ObjectStrings::commonPart(std::string s1, std::string s2, int len) {
    std::string s_res;

    for (int i = 0; i < len; i++) {
        s_res += s1[i];
        if (s_res[i] == ' ') s_res[i] = s2[i];
    }

    return s_res;
}
